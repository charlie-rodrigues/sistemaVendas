    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // botão buscar produto por nome
      String nome = txtDescricao.getText();
      Produto obj = new Produto();
      ProdutoDAO dao = new ProdutoDAO();
      obj = dao.buscarProduto(nome);
      
      cbFornecedorjComboBox1.removeAllItems();
      if(obj.getDescricao()!=null){
      txtCodigo.setText(String.valueOf(obj.getId()));
      txtDescricao.setText(obj.getDescricao());
      txtPreco.setText(String.valueOf(obj.getPreco()));
      txtQtdEstoque.setText(String.valueOf(obj.getQtd_estoque()));
      
      Fornecedor f = new Fornecedor();
      FornecedorDAO daof = new FornecedorDAO();
      f= daof.buscarFornecedor(obj.getFornecedor().getNome());
      cbFornecedorjComboBox1.getModel().setSelectedItem(f);
      }else{
      JOptionPane.showMessageDialog(null,"produto não encontrado!");
      }

    }//GEN-LAST:event_btnPesquisarActionPerformed

