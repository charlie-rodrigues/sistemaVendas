    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // botão add item
        String nome = txtProduto.getText();
       
        
        Produto obj = new Produto();
        ProdutoDAO dao = new ProdutoDAO();
        obj = dao.buscarProduto(nome);
       
        if (obj.getDescricao() != null) {
             int estoque = Integer.valueOf(txtEstoque.getText());
             int quantidade = Integer.valueOf(txtQTD.getText());
            preco = Double.valueOf(txtPreco.getText());
            qtd = Integer.valueOf(txtQTD.getText());
            subtotal = preco * qtd;
            total += subtotal;
              if(estoque >= quantidade){
            txtTotalVenda.setText(String.valueOf(total));
          
            carrinho = (DefaultTableModel) tabelaCarrinhoDeCompras.getModel();
           
            carrinho.addRow(new Object[]{
                txtCodigo.getText(),
                txtProduto.getText(),
                txtQTD.getText(),
                txtPreco.getText(),
                subtotal

            });
            }else{
            JOptionPane.showMessageDialog(null,"não foi possível adicionar no carrinho de compras \n NÃO TEMOS ESSA QUANTIDADE NO ESTOQUE!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "faltam informações inportantes a serem preenchidas! \n Verifique os dados do produto!");
            txtCodigo.requestFocus();
        }

    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnPagamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagamentoActionPerformed
        // botão pagamentos
        String nome = txtnome.getText();
        String cpf = txtcpf.getText();

        Cliente obj = new Cliente();

        ClientesDAO dao = new ClientesDAO();

        obj = dao.BuscarCliente(nome);
        obj = dao.buscarClientePorCpf(cpf);
        if (obj.getCpf() != null && obj.getNome() != null) {
            FormPagamentos telaPag = new FormPagamentos();
            telaPag.txtTotal.setText(String.valueOf(total));
            telaPag.cliente = obj;
            telaPag.carrinho = carrinho;
            telaPag.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "faltam dados importantes a serem preenchidos!!! Verifique essas informações!!! "
                    + "\n Essas informações podem ser NOME e/ou CPF do CLIENTE!");
            txtcpf.requestFocus();
        }

    }//GEN-LAST:event_btnPagamentoActionPerformed

    private void txtPesquisarProdutojTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisarProdutojTextField1KeyReleased
        // TODO add your handling code here:
        // pesquisar digitando o nome
        String nome = "%" + txtPesquisarProdutojTextField1.getText() + "%";
        ProdutoDAO dao = new ProdutoDAO();
        List<Produto> lista = dao.pesquisarProdutoPorNome(nome);
        DefaultTableModel dados = (DefaultTableModel) tabelaProdutosVendajTable1.getModel();
        dados.setNumRows(0);
        for (Produto p : lista) {
            dados.addRow(new Object[]{
                p.getId(),
                p.getDescricao(),
                p.getPreco(),
                p.getQtd_estoque(),
                p.getFornecedor().getNome()
            });

        }
    }//GEN-LAST:event_txtPesquisarProdutojTextField1KeyReleased

