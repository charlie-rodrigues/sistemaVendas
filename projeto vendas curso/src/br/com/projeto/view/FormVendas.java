    private void txtPorcentagemjFormattedTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPorcentagemjFormattedTextField1KeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) { // descontos em %
            float precoComPorcentagem;
            float multiplicacao;
            float porcentagem = Float.valueOf(txtPorcentagemjFormattedTextField1.getText());
            float preco = Float.valueOf(txtPreco.getText());
            multiplicacao = preco * porcentagem;
            precoComPorcentagem = preco - (multiplicacao);
            txtPreco.setText(String.valueOf(precoComPorcentagem));
            txtPorcentagemjFormattedTextField1.disable();
            txtQTD.requestFocus();
        }
    }//GEN-LAST:event_txtPorcentagemjFormattedTextField1KeyPressed

